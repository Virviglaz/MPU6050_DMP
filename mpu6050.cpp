int MPU6050_Base::Init()
{
    ResetIC();

    uint8_t who_am_i;
    ifs_.Read(MPU6050_RA_WHO_AM_I, who_am_i);
    who_am_i &= 0x7E;
    who_am_i >>= 1;
    if (who_am_i != 0x34)
        return -ENODEV;

    /* Minimal configuration */
    ifs_.Write(MPU6050_RA_PWR_MGMT_1, 1);
    ifs_.Write(MPU6050_RA_PWR_MGMT_2, 0);
    ifs_.Write(MPU6050_RA_SMPLRT_DIV, 0x07);
    ifs_.Write(MPU6050_RA_INT_PIN_CFG, 0x10);
    ifs_.Write(MPU6050_RA_INT_ENABLE, DATA_READY_BIT);
    return 0;
}

bool MPU6050_Base::IsDataReady()
{
    uint8_t status;
    ifs_.Read(MPU6050_RA_INT_STATUS, status);
    return status & DATA_READY_BIT;
}

MPU6050_Base::RawData MPU6050_Base::WaitForData()
{
    while (!IsDataReady()) {};
    return GetData();
}

void MPU6050_Base::SetAccGain(acc_gain gain)
{
    ifs_.Write(MPU6050_RA_ACCEL_CONFIG, static_cast<uint8_t>(gain));
    _acc_gain = gain;
}

void MPU6050_Base::SetGyroGain(gyro_gain gain)
{
    ifs_.Write(MPU6050_RA_GYRO_CONFIG, static_cast<uint8_t>(gain));
    _gyro_gain = gain;
}

void MPU6050_Base::SetSampleRate(uint8_t sample_rate_hz)
{
    ifs_.Write(MPU6050_RA_SMPLRT_DIV, 1000 / sample_rate_hz - 1);
}

void MPU6050_Base::SetFilterOrder(uint8_t filter_order)
{
    ifs_.Write(MPU6050_RA_CONFIG, filter_order & 0x07);
}

void MPU6050_Base::EnableDataReadyInterrupt()
{
    /* LATCH_INT_EN + INT_RD_CLEAR */
    ifs_.Write(MPU6050_RA_INT_PIN_CFG, 0x30);
}

MPU6050_Base::RawData MPU6050_Base::GetData()
{
    RawData data(_acc_gain, _gyro_gain);
    ifs_.Read(MPU6050_RA_ACCEL_XOUT_H, (uint8_t*)&data, sizeof(RawData));

    data.x = BigEndianToNative(data.x);
    data.y = BigEndianToNative(data.y);
    data.z = BigEndianToNative(data.z);
    data.temp = BigEndianToNative(data.temp);
    data.ax = BigEndianToNative(data.ax);
    data.ay = BigEndianToNative(data.ay);
    data.az = BigEndianToNative(data.az);

    return data;
}

float MPU6050_Base::RawData::GetTemperature() const
{
    return (temp / 340.0f) + 36.53f;
}

/* Convert to real data */
float MPU6050_Base::RawData::GetAccX() const { return static_cast<float>(x) / (16384.0f / (1 << _acc_gain)); }
float MPU6050_Base::RawData::GetAccY() const { return static_cast<float>(y) / (16384.0f / (1 << _acc_gain)); }
float MPU6050_Base::RawData::GetAccZ() const { return static_cast<float>(z) / (16384.0f / (1 << _acc_gain)); }
float MPU6050_Base::RawData::GetGyroX() const { return static_cast<float>(ax) / (131.0f / (1 << _gyro_gain)); }
float MPU6050_Base::RawData::GetGyroY() const { return static_cast<float>(ay) / (131.0f / (1 << _gyro_gain)); }
float MPU6050_Base::RawData::GetGyroZ() const { return static_cast<float>(az) / (131.0f / (1 << _gyro_gain)); }

void MPU6050_Base::SetBit(uint8_t reg, uint8_t bit_mask)
{
    uint8_t value;
    ifs_.Read(reg, value);
    value |= bit_mask;
    ifs_.Write(reg, value);
}

void MPU6050_Base::ClearBit(uint8_t reg, uint8_t bit_mask)
{
    uint8_t value;
    ifs_.Read(reg, value);
    value &= ~bit_mask;
    ifs_.Write(reg, value);
}

int16_t MPU6050_Base::ReadReg_s16(uint8_t reg)
{
    int16_t value;
    ifs_.Read(reg, reinterpret_cast<uint8_t*>(&value), 2);
    return BigEndianToNative(value);
}

void MPU6050_Base::WriteReg_s16(uint8_t reg, int16_t value)
{
    int16_t be_value = NativeToBigEndian(value);
    ifs_.Write(reg, reinterpret_cast<uint8_t*>(&be_value), 2);
}

void MPU6050_Base::ReadFIFO(uint8_t *buf, size_t size)
{
    ifs_.Read(MPU6050_RA_FIFO_R_W, buf, size);
}

uint16_t MPU6050_Base::GetFIFOCount()
{
    uint16_t fifo_count;
    ifs_.Read(MPU6050_RA_FIFO_COUNTH, (uint8_t*)&fifo_count, sizeof(fifo_count));
    return BigEndianToNative(fifo_count);
}

void MPU6050_Base::ResetIC()
{
    SetBit(MPU6050_RA_PWR_MGMT_1, DEVICE_RESET);
    while (1) {
        uint8_t value;
        ifs_.Read(MPU6050_RA_PWR_MGMT_1, value);
        if ((value & DEVICE_RESET) == 0)
            break;
    }
}

MPU6050_Base::cal_offsets MPU6050_Base::ReadCalibrationOffsets()
{
    cal_offsets offsets;
    uint8_t buf[6];

    // 1. Safe packet read from I2C into a local byte array (No Strict Aliasing violation)
    ifs_.Read(MPU6050_RA_XA_OFFS_H, buf, 6);
    
    // 2. Reassemble sign-correct values from Big-Endian array
    // We do sign extension by casting the high byte to int8_t before shifting
    offsets.acc_x_offset = ((static_cast<int16_t>(static_cast<int8_t>(buf[0])) << 8) | buf[1]) >> 1;
    offsets.acc_y_offset = ((static_cast<int16_t>(static_cast<int8_t>(buf[2])) << 8) | buf[3]) >> 1;
    offsets.acc_z_offset = ((static_cast<int16_t>(static_cast<int8_t>(buf[4])) << 8) | buf[5]) >> 1;

    // 3. Safe packet read for Gyroscope
    ifs_.Read(MPU6050_RA_XG_OFFS_USRH, buf, 6);
    offsets.gyro_x_offset = (static_cast<int16_t>(static_cast<int8_t>(buf[0])) << 8) | buf[1];
    offsets.gyro_y_offset = (static_cast<int16_t>(static_cast<int8_t>(buf[2])) << 8) | buf[3];
    offsets.gyro_z_offset = (static_cast<int16_t>(static_cast<int8_t>(buf[4])) << 8) | buf[5];

    return offsets;
}

void MPU6050_Base::WriteCalibrationOffsets(const cal_offsets& offsets)
{
    uint8_t buf[6];

    // 1. Pack Accelerometer offsets safely (Using const input, no user data corruption)
    // Shift left by 1 and force the temperature compensation bit to 1
    int16_t x = (offsets.acc_x_offset << 1) | 1;
    int16_t y = (offsets.acc_y_offset << 1) | 1;
    int16_t z = (offsets.acc_z_offset << 1) | 1;

    buf[0] = static_cast<uint8_t>((x >> 8) & 0xFF);  buf[1] = static_cast<uint8_t>(x & 0xFF);
    buf[2] = static_cast<uint8_t>((y >> 8) & 0xFF);  buf[3] = static_cast<uint8_t>(y & 0xFF);
    buf[4] = static_cast<uint8_t>((z >> 8) & 0xFF);  buf[5] = static_cast<uint8_t>(z & 0xFF);
    ifs_.Write(MPU6050_RA_XA_OFFS_H, buf, 6);

    // 2. Pack Gyroscope offsets safely
    buf[0] = static_cast<uint8_t>((offsets.gyro_x_offset >> 8) & 0xFF); buf[1] = static_cast<uint8_t>(offsets.gyro_x_offset & 0xFF);
    buf[2] = static_cast<uint8_t>((offsets.gyro_y_offset >> 8) & 0xFF); buf[3] = static_cast<uint8_t>(offsets.gyro_y_offset & 0xFF);
    buf[4] = static_cast<uint8_t>((offsets.gyro_z_offset >> 8) & 0xFF); buf[5] = static_cast<uint8_t>(offsets.gyro_z_offset & 0xFF);
    ifs_.Write(MPU6050_RA_XG_OFFS_USRH, buf, 6);

    // 3. Reset hardware FIFO and DMP pipeline to instantly apply new parameters
    SetBit(MPU6050_RA_USER_CTRL, 0x0C);
}

